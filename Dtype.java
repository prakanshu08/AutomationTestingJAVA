public class Dtype {
    public static void main(String[] args) {
        //initialize int var
        int i = 5;
        System.out.println("integer is: " + i);

        //initialize double var
        double d = 3.14567;
        System.out.println("Double is: " + d);

        //initialize char type var
        char c = 'J';
        System.out.println("Character is: " + c);

        //initialize boolean var
        boolean fanOff = false;
        System.out.println("Boolean is:" +fanOff);

        //initialize String var
        String s = "Hello Automation";
        System.out.println("String is :" + s);
    }
}
